

def acfr(A: [[]]):

    Rook = 'R'
    #space = '.'
    Bishop = 'B'
    pawn = 'p'

    rc = 0
    l = 0

    for r in A:
        if Rook in r:
            x, y = rc, r.index(Rook)
            break
        rc += 1
    
    #---move right
    for sq in A[x][y:]:
        if sq == Bishop:
            break
        elif sq == pawn:
            l += 1
            break

    #---move left
    for sq in A[x][y-1::-1]:
        if sq == Bishop:
            break
        elif sq == pawn:
            l += 1
            break
    
    #---move up
    for xx in range(x-1,-1,-1):
        if A[xx][y] == Bishop:
            break
        elif A[xx][y] == pawn:
            l += 1
            break

    #---move down
    for xx in range(x+1,8):
        if A[xx][y] == Bishop:
            break
        elif A[xx][y] == pawn:
            l += 1
            break

    return l

